function cl = naca0012_cl
%NACA0012  Gets lift and drag function_handles for the NACA 12% 
%          symmetric airfoil
%
%   [CL,CD] = NACA0012 gets the lift and drag function_handles
%   of the symmetric NACA airfoil of 12% relative thickness 
%

data    = cldataNaca0012;

cl      = @(a,Re) clNaca0012(a,data);


function cl = clNaca0012(alpha,datanaca)

alpha = rem(alpha,2*pi);
region_p = alpha>=0;
region_n = alpha<0;
alpha(region_p)    = alpha(region_p);
alpha(region_n)    = 2*pi+alpha(region_n);

alpha_d  = datanaca(:,1);
cl_d     = datanaca(:,2);
cl       = interp1(alpha_d,cl_d,alpha);



