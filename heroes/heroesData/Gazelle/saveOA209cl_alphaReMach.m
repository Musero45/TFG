function [alpha3D,log10Re3D,M3D,coeff3D] = coeffOA2092coeff3d(coeffOA209,coeff)
% This function gets the input structure coeffOA209 which includes fields 
% with the lift, drag and moment coefficients and transforms the field
% defined by the string coeff into 3D matrices of angle of attack, logrithm
% of Reynolds, Mach and the aerodynamic coefficient itself, alpha3D, 
% log10Re3D,M3D and coeff3D, respectively. 



%% Definition of the set of aerodynamic coeffcient matrices
% Load original data
alpha     = coeffOA209.alpha(:);
na        = length(alpha);
nm        = length(coeffOA209.Mach);


% Definition of LOG10Re matrix
% Because there is no information about the Reynolds number we just define
% two values of Reynolds which are meaningless
nL10Re      = 3;
L10Re1      = 0;
L10Re2      = 20;
log10Re3D   = linspace(L10Re1,L10Re2,nL10Re);
log10Re3D   = log10Re3D(ones(na,1),:,ones(nm,1));

% Definition of alpha matrix
alpha3D   = alpha(:,ones(nL10Re,1),ones(nm,1));

% Definition of MACH matrix
Mach        = reshape(coeffOA209.Mach(:),[1,1,nm]);
M3D         = repmat(Mach,[na,nL10Re]);


% Definition of aerodynamic coefficient matrix
coeff       = reshape(coeffOA209.(coeff),[na,1,nm]);
coeff3D     = coeff(:,ones(nL10Re,1),:);
end
