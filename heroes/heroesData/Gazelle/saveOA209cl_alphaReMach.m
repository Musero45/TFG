function saveOA209cl_alphaReMach
% saveOA209cl_alphaReMach is a script to build up the precompiled 
% aerodynamic data file OA209ARMdata.mat. It is in the heroes 
% distribution just because of completeness sake.
%
%

% Definition of the set of CL matrices
clOA209                            = cldataOA209;
[alpha3Dcl,log10Re3Dcl,M3Dcl,cl3D] = coeffOA2092coeff3d(clOA209,'cl');

% Definition of the set of CD matrices
cdOA209                            = cddataOA209;
[alpha3Dcd,log10Re3Dcd,M3Dcd,cd3D] = coeffOA2092coeff3d(cdOA209,'cd');

% Definition of the set of CM matrices
% FIXME dummy CM just for 
cmOA209                            = cmdataOA209;
[alpha3Dcm,log10Re3Dcm,M3Dcm,cm3D] = coeffOA2092coeff3d(cmOA209,'cm');

readme         = ['File created by saveOA209_alphaReMach'...
                  'original data comes from NASA-TM-88370' ];
ARMdata        = struct('Readme',readme,... 
                        'ALPHA3D_CL',alpha3Dcl,...
                        'Log10Re3D_CL',log10Re3Dcl,...
                        'M3D_CL',M3Dcl,...
                        'ALPHA3D_CD',alpha3Dcd,...
                        'Log10Re3D_CD',log10Re3Dcd,...
                        'M3D_CD',M3Dcd,...
                        'ALPHA3D_CM',alpha3Dcm,...
                        'Log10Re3D_CM',log10Re3Dcm,...
                        'M3D_CM',M3Dcm,...
                        'CL3D',cl3D,...
                        'CD3D',cd3D,...
                        'CM3D',cm3D);
 
name = 'OA209ARMdata';              
save (name,'-struct','ARMdata');  
 

end


